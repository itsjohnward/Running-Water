void ClassDemoApp::Update(float elapsed) {
    if(state == STATE_GAME) {
        for(int i = 0; i < brushes.size(); i++) {
            if ((brushes[i]->trigger == 1) && (player->collision(brushes[i]))) {
                levelComplete();
                std::cout << "level complete" << std::endl;
            }
        }
    
        player->FixedUpdate(FIXED_TIMESTEP, brushes);
        for(int i = 0; i < sprites.size(); i++) {
            sprites[i]->FixedUpdate(FIXED_TIMESTEP, brushes);
        }
        for(int i = 0; i < debris.size(); i++) {
            debris[i]->y -= FIXED_TIMESTEP;
            debris[i]->rotation+=50*FIXED_TIMESTEP;
            if(player->collision(debris[i])){
                state = STATE_DEAD;
            }
        }
        debrisFall();
    }
    
    playerControls();
    
}

bool ClassDemoApp::UpdateAndRender() {
    
    ticks = (float)SDL_GetTicks()/1000.0f;
    elapsed = ticks - lastFrameTicks;
    lastFrameTicks = ticks;
    
    float fixedElapsed = (elapsed + timeLeftOver);
    if(fixedElapsed > FIXED_TIMESTEP * MAX_TIMESTEPS) {
        fixedElapsed = FIXED_TIMESTEP * MAX_TIMESTEPS;
    }
    while (fixedElapsed >= FIXED_TIMESTEP ) {
        fixedElapsed -= FIXED_TIMESTEP;
        Update(fixedElapsed);
    }
    timeLeftOver = fixedElapsed;
    
    
    
    
    
    Render();
    
    return done;
}

