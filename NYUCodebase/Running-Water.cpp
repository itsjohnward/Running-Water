void ClassDemoApp::playerSpawn() {
    player = new Sprite(water_texture, 0.0, -0.75, 2.0/LEVEL_SCALE, 1.0/LEVEL_SCALE, 0.0, true);
    player->y_acceleration = -5;
    player->move_speed = 2;
}

void ClassDemoApp::buildLevel() {
    brushes.clear();
    for(int row = 0; row < level.size(); row++) {
        for(int column = 0; column < level[row].size(); column++) {
            if(level[row][column]==0) {
                brushes.push_back(new Brush(floor_texture2, (float)column/LEVEL_SCALE-(float)(1-(float)1/LEVEL_SCALE), (float)row/LEVEL_SCALE-(float)(1-(float)1/LEVEL_SCALE)+(float)1/LEVEL_SCALE, 1.0/LEVEL_SCALE, 1.0/LEVEL_SCALE, 0.0, true, false));
            }
            else if(level[row][column]==2) {
                brushes.push_back(new Brush(goal_texture, (float)column/LEVEL_SCALE-(float)(1-(float)1/LEVEL_SCALE), (float)row/LEVEL_SCALE-(float)(1-(float)1/LEVEL_SCALE)+(float)1/LEVEL_SCALE, 1.0/LEVEL_SCALE, 1.0/LEVEL_SCALE, 0.0, true, false));
                brushes[brushes.size()-1]->trigger = 1;
            }
            else if(level[row][column]==1) {
                brushes.push_back(new Brush(floor_texture, (float)column/LEVEL_SCALE-(float)(1-(float)1/LEVEL_SCALE), (float)row/LEVEL_SCALE-(float)(1-(float)1/LEVEL_SCALE), 1.0/LEVEL_SCALE, 1.0/LEVEL_SCALE, 0.0));
            }
        }
    }
    for(int i = 0; i < brushes.size(); i++) {
        brushes[i]->friction = 5;
    }
    playerSpawn();
}

void ClassDemoApp::drawLevel() {
    for( int i = 0; i < brushes.size(); i++ ) {
        brushes[i]->draw();
    }
}

void ClassDemoApp::levelComplete() {
    std::cout << state_level << std::endl;
    if(state_level==1 || state_level==2) {
        state_level++;
        if(state_level==2) {
            levelTwo();
            buildLevel();
        }
        if(state_level==3) {
            levelThree();
            buildLevel();
        }
    }
    else if(state_level==3) {
        state = STATE_MENU_END;
    }
}

void ClassDemoApp::drawSprites() {
    player->draw();
    for( int i = 0; i < sprites.size(); i++ ) {
        sprites[i]->draw();
    }
}

void ClassDemoApp::playerControls() {
    while (SDL_PollEvent(&event)) {
        if (event.type == SDL_QUIT || event.type == SDL_WINDOWEVENT_CLOSE) {
            done = true;
        }
        else if(event.type == SDL_KEYDOWN) {
            if (state == STATE_MENU) {
                if(event.key.keysym.scancode == SDL_SCANCODE_SPACE) {
                    state = STATE_GAME;
                }
                else if(event.key.keysym.scancode == SDL_SCANCODE_ESCAPE) {
                    done = true;
                }
                else if(event.key.keysym.scancode == SDL_SCANCODE_R) {
                    Init();
                }
            }
            else if (state == STATE_GAME) {
                if(event.key.keysym.scancode == SDL_SCANCODE_SPACE && (player->bottom_collided)) {
                    Mix_PlayChannel( -1, jump, 0);
                    player->y_speed = 2.5;
                }
                if(event.key.keysym.scancode == SDL_SCANCODE_ESCAPE) {
                    state = 0;
                }
            }
            if (state == STATE_MENU_END) {
                if(event.key.keysym.scancode == SDL_SCANCODE_ESCAPE) {
                    done = true;
                }
                else if(event.key.keysym.scancode == SDL_SCANCODE_R) {
                    Init();
                }
            }
            if (state == STATE_DEAD) {
                if(event.key.keysym.scancode == SDL_SCANCODE_ESCAPE) {
                    done = true;
                }
                else if(event.key.keysym.scancode == SDL_SCANCODE_R) {
                    Init();
                }
            }
        }
    }
    
    player->x_acceleration = 0;
    keys = SDL_GetKeyboardState(NULL);
    if(keys[SDL_SCANCODE_A]) {
        player->moveLeft();
    }
    if(keys[SDL_SCANCODE_D]) {
        player->moveRight();
    }
}

